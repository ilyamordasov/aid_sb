function hostToTitle(host: string): string {
  const base = host.replace(/^www\./, '')
  const first = base.split('.')[0] ?? host
  if (first.length <= 3) return first.toUpperCase()
  return first
    .split('-')
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(' ')
}

